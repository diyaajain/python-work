cars = ['bmw', 'audi', 'toyota', 'subaru']

cars.sort()
print(f'The sorted list is: {cars}')

cars.sort(reverse=True)
print(f'The reverse sorted list is: {cars}')

cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.reverse()
print(f'The reversed list is: {cars}')

cars = ['bmw', 'audi', 'toyota', 'subaru']
length = len(cars)
print(f'The length of the list is: {length}')